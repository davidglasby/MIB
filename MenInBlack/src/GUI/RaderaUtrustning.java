    public void fyllVapenLista()
    {
        var vapenLista = infoService.getAllaVapen();
        DefaultListModel vapen = new DefaultListModel();
        jlVapen.setModel(vapen);
        for(int i = 0; i < vapenLista.size(); i++)
        {
            vapen.add(i, vapenLista.get(i).getBenamning());
        }
    }
    
    public void fyllTeknikLista()
    {
        var teknikLista = infoService.getAllTeknik();
        DefaultListModel teknik = new DefaultListModel();
        jlTeknik.setModel(teknik);
        for(int i = 0; i < teknikLista.size(); i++)
        {
            teknik.add(i, teknikLista.get(i).getBenamning());
        }
    }
    
    public void fyllKommunikationLista()
    {
        var kommunikationLista = infoService.getAllKommunikation();
        DefaultListModel kommunikation = new DefaultListModel();
        jlKommunikation.setModel(kommunikation);
        if(kommunikationLista != null)
        {
            for(int i = 0; i < kommunikationLista.size(); i++)
            {
                kommunikation.add(i, kommunikationLista.get(i).getBenamning());
            }
        }
    }
    
    
    

