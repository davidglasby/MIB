    private void btnRaderaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRaderaActionPerformed
         // TODO add your handling code here:
         /* Hämtar alla valda String-värden från listorna
            Om värden inte är null anropas metoder för att
            radera utrustning både från VAPEN, TEKNIK och/eller KOMMUNIKATIONS Tabellerna
            och från UTRUSTNING tabellen */
            
         String vapen = jlVapen.getSelectedValue();
         String teknik = jlTeknik.getSelectedValue();
         String kommunikation = jlKommunikation.getSelectedValue();
         if(vapen != null)
         {
             String vapenTabell = "VAPEN";
             int vapenid = infoService.getUtrustningsIDfromBenamning(vapen);
             infoService.raderaFromVapenTeknikOrKommunikation(vapenid, vapenTabell);
             infoService.raderaFromUtrustning(vapenid);
         }
         if(teknik != null)
         {
             String teknikTabell = "TEKNIK";
             int teknikid = infoService.getUtrustningsIDfromBenamning(teknik);
             infoService.raderaFromVapenTeknikOrKommunikation(teknikid, teknikTabell);
             infoService.raderaFromUtrustning(teknikid);
         }
         if(kommunikation != null)
         {
             String kommunikationTabell = "KOMMUNIKATION";
             int kommunikationID = infoService.getUtrustningsIDfromBenamning(kommunikation);
             infoService.raderaFromVapenTeknikOrKommunikation(kommunikationID, kommunikationTabell);
             infoService.raderaFromUtrustning(kommunikationID);
         }
         
    }//GEN-LAST:event_btnRaderaActionPerformed

