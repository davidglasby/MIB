    public void fyllOmradesLista()
    {
        var allaOmraden = infoService.getAllaOmraden();
        DefaultListModel omraden = new DefaultListModel();
        jlOmraden.setModel(omraden);
        for(int i = 0; i < allaOmraden.size(); i++)
        {
            omraden.add(i, allaOmraden.get(i).getBenamning());
        }
    }
    
    public void fyllAgentLista()
    {
        var agentLista = agentService.getAllaAgenter();
        DefaultListModel agenter = new DefaultListModel();
        jlAgenter.setModel(agenter);
        for(int i = 0; i < agentLista.size(); i++)
        {
            agenter.add(i, agentLista.get(i).getNamn());
        }
    }

