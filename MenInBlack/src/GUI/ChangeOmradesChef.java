    private void btnSparaChefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSparaChefActionPerformed
        // TODO add your handling code here:
        var omrade = jlOmraden.getSelectedValue();
        var agent = jlAgenter.getSelectedValue();
        int omradesID = Integer.parseInt(infoService.getIDfromOmrade(omrade));
        int agentID = Integer.parseInt(agentService.getIDfromNamn(agent));
        infoService.andraOmradesChef(omradesID, agentID);
        JOptionPane.showMessageDialog(null, agent + " är nu chef över " + omrade);
        JOptionPane.showMessageDialog(null, agent + " är inte längre chef över sitt tidigare område");
        
    }//GEN-LAST:event_btnSparaChefActionPerformed

