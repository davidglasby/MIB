    private void btnSparaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSparaActionPerformed
        // TODO add your handling code here:
        int id = agentService.getMaxAgentID();
        String namn = txtNamn.getText();
        String telefon = txtTelefon.getText();
        String anstDatum = txtAnstDatum.getText();
        String admin = txtAdmin.getText();
        String losenord = txtLosenord.getText();
        int omradesid = Integer.parseInt(txtOmradesID.getText());
        
        agentService.skapaAgent(id, namn, telefon, anstDatum, admin, losenord, omradesid);
        if(ckbxFaltAgent.isSelected())
        {
            agentService.registreraFaltAgent(id);
        }
        
        var utrustning = jlUtrustningLista.getSelectedValue();
        if(utrustning != null)
        {
            var utrustningsid = infoService.getUtrustningsIDfromBenamning(utrustning);
            infoService.tilldelaAgentUtrustning(utrustningsid, id, anstDatum);
        }
        
        JOptionPane.showMessageDialog(null, namn + " är nu registrerad! han har automatiskt blivit tilldelad med ID-nummer: " + id);
        
        
    }//GEN-LAST:event_btnSparaActionPerformed

