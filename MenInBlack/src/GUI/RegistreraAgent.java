    public void fyllOmradesLista()
    {
        var omradeslista = infoService.getAllaOmraden();
        DefaultListModel lista = new DefaultListModel();
        jlOmradeLista.setModel(lista);
        for(int i = 0; i<omradeslista.size(); i++)
        {
            lista.add(i, omradeslista.get(i).getBenamning());
        }
    } 
    
    public void fyllUtrustningsLista()
    {
        var utrustningsLista = infoService.getAllTillgangligUtrustning();
        if (utrustningsLista != null)
        {
            DefaultListModel lista = new DefaultListModel();
            jlUtrustningLista.setModel(lista);
            for(int i = 0; i < utrustningsLista.size(); i++)
            {
                lista.add(i, utrustningsLista.get(i).getBenamning());
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Igen utrustning tillgänglig, registrera utrustning för att tilldela agenten utrustning");
        }
    }
    
    public void fyllFordonsLista()
    {
        var fordonsLista = infoService.getAllaTillgangligaFordon();
        if(fordonsLista != null)
        {
            DefaultListModel lista = new DefaultListModel();
            jlFordonLista.setModel(lista);
            for(int i = 0; i < fordonsLista.size(); i++)
            {
                lista.add(i, fordonsLista.get(i).getFordonsBeskrivning());
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Inga fordon tillgängliga");
        }
               
        
    }
    

